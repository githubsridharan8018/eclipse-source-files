package com.hcl;

public class Evenorodd {
	
	public static void main(String[] args) {
		
	
	      int [] a= {20,15,33,60};
	      
	      for (int i = 0; i <=3; i++) {
		
	       if (a[i]%2==0) {
	     
	    	   System.out.println("even" +" " +a[i]);
	       }
		
	       else {
         
	    	   System.out.println("odd" +" " +a[i]);
	            }
	      }
	      
	}
}
	